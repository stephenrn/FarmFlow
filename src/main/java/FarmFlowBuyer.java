    private void addToCart(ItemsClass item) {
        DefaultTableModel cartList = (DefaultTableModel) tblCart.getModel();

        // ADD ITEM TO THE CART TABLE
        Object row[] = {
            item.getItemName(),
            item.getItemCategory(),
            item.getItemQuantity(),
            item.getItemPrice(),
            item.getItemTotalPrice()
        };
        cartList.addRow(row);
        xrow++;

        // UPDATE ITEM COUNT
        itemCount = itemCount + item.getItemQuantity();
        txtRedTotalItemCount.setText("" + itemCount);
    }
    
    private void updateCartTotal() {
        DefaultTableModel cartList = (DefaultTableModel) tblCart.getModel();
        double tot = 0;

        // CALCULATE THE TOTAL PRICE FROM THE TABLE
        for (int i = 0; i < cartList.getRowCount(); i++) {
            tot += Double.valueOf(cartList.getValueAt(i, 4).toString());
        }

        // UPDATE TOTAL PRICE DISPLAY
        txtTabOverallPrice.setText("₱" + tot);
        txtOverallPrice.setText("₱" + tot);
    }


    
