    private void butEditItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butEditItemActionPerformed
              //EDIT SELECTED ROW ON TAB
        DefaultTableModel cartList = (DefaultTableModel) tblCart.getModel();
        int row = tblCart.getSelectedRow();
        if (row>=0){
        int newQuantity = Integer.parseInt(JOptionPane.showInputDialog(null,"Enter product quantity:",
                "Edit Quantity", JOptionPane.QUESTION_MESSAGE));
        if (newQuantity>0){
        itemCount -= Integer.parseInt(cartList.getValueAt(row, 2).toString());
        tblCart.setValueAt(newQuantity, row, 2);
        itemCount += Integer.parseInt(cartList.getValueAt(row, 2).toString());

        double tot = 0;
        for (int i=0; i < cartList.getRowCount(); i++){
            tot+=Double.valueOf(cartList.getValueAt(i, 4).toString());
        }
        tot-= Double.valueOf(cartList.getValueAt(row, 4).toString());
        double price = Double.valueOf(cartList.getValueAt(row, 3).toString());
        double newPrice = newQuantity * price;
        cartList.setValueAt(newPrice,row,4);
        tot+= Double.valueOf(cartList.getValueAt(row, 4).toString());
        txtTabOverallPrice.setText("₱"+tot);
        txtOverallPrice.setText("₱"+tot);
        txtItemCount.setText(""+itemCount);
        txtRedTotalItemCount.setText(""+itemCount);}
        else {
            JOptionPane.showMessageDialog(null, "Invalid quantity");
        }
        
        }
        else {
                JOptionPane.showMessageDialog(null, "Please select an order to edit");
                }
    }//GEN-LAST:event_butEditItemActionPerformed

    
    private void butPurchaseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butPurchaseActionPerformed
  //PURCHASE ALL ITEMS ON CART
   DefaultTableModel cartList = (DefaultTableModel) tblCart.getModel();
   ComputationClass compute = new ComputationClass();
   
   String items = "";

    if (cartList.getRowCount() == 0) {
        JOptionPane.showMessageDialog(null, "Your cart is empty. Add items before purchasing.");
        return;
    }
    purchase.setLocation(txtLocation.getText());
   if (purchase.getPayment() == null || purchase.getPayment().isEmpty() ||
    purchase.getDelivery() == null || purchase.getDelivery().isEmpty() ||
    purchase.getLocation() == null || purchase.getLocation().isEmpty()) {
    
    JOptionPane.showMessageDialog(null, "Please fill out the needed information first before purchasing");
    return;
}

 
    String type[] = {"Regular", "Student", "Senior Citizen"};
    Object select = JOptionPane.showInputDialog(null, "Type of customer", "Discount", JOptionPane.QUESTION_MESSAGE,
            null,
            type,
            type[0]);

    double total = 0;

    for (int i = 0; i < cartList.getRowCount(); i++) {
        String productName = cartList.getValueAt(i, 0).toString();
        double price = Double.parseDouble(cartList.getValueAt(i, 3).toString());
        int quantity = Integer.parseInt(cartList.getValueAt(i, 2).toString());
        double subtotal = Double.parseDouble(cartList.getValueAt(i, 4).toString());
        
        
        items += productName + "\t₱" + price + "\t     " + quantity + "\t₱" + subtotal + "\n";
        total += subtotal;
    }
    double percentage = compute.getDiscount(select.toString());
    double discount = compute.getDiscount(percentage, total);
    double shippingFee = compute.getShippingFee(purchase.getDelivery());
    double newGrandTotal = compute.getNewGrandTotal(total, discount, shippingFee);
    String payment = purchase.getPayment();
    String delivery = purchase.getDelivery();
    String address  = purchase.getLocation();
    String username = purchase.getUserName();

   AbstractClass receipt = new ReceiptClass(items,total, discount, shippingFee, newGrandTotal, 
           payment,delivery, address, username);
   String receiptText = receipt.viewReceipt(items,total, discount, shippingFee, newGrandTotal, 
           payment,delivery, address, username);
    txtReceipt.setText(receiptText);
    
    //ADD TO ORDERS OF ADMIN
    new OrdersClass(purchase.getUserName(), itemCount, newGrandTotal, receiptText);
    
    //clearing 
    cartList.setRowCount(0);
    txtOverallPrice.setText("₱0.00");
    txtTabOverallPrice.setText("₱0.00");
    txtItemCount.setText("0");
    txtRedTotalItemCount.setText("0");
    itemCount = 0;
    
    }//GEN-LAST:event_butPurchaseActionPerformed

